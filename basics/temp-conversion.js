// https://www.rapidtables.com/convert/temperature/fahrenheit-to-kelvin.html
let fahrenheit = 32

let celcius = (fahrenheit - 32) * (5 / 9)
console.log('In Celcius:', celcius)

let kelvin = (fahrenheit + 459.67) * (5 / 9)
console.log('In Kelvin:', kelvin)
